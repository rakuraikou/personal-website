      </h1>
      <hr>
      <p>
        Life is present everywhere on Luxos, from the peaks of Guashan Range to the depths of the Luxic Sea. All things, living and nonliving, contain some quantity and quality of light. However, only living organisms have the ability to manipulate light through an aspect.
      </p>
      <h2>
        Classifying Life Forms
      </h2>
      <hr>
      <p>
        Because of the potential to cause harm with an aspect, the general behavior of living beings on Luxos (particularly its fauna) is important to grade in a meaningful and concise manner. Scholars have determined an "Response Index" system to classify the expected response a given creature may have towards an approaching person. Response Index is indicated on documents using the first letter of the classification name.
      </p>
      <ul>
        <li>
          <b>Timid (<span style="color: #3794ff">T</span>):</b> The creature is very likely to flee and/or hide itself upon seeing a person, especially one who is larger or otherwise more imposing than itself.
        </li>
        <li>
          <b>Docile (<span style="color: #21af1d">D</span>):</b> The creature does not respond strongly to the presence of a person. Some under this classification may even approach an individual out of curiosity.
        </li>
        <li>
          <b>Reactive (<span style="color: #e4d72b">R</span>):</b> The creature, similar to a <b>timid</b> one, is wary of the approaching person. However, instead of immediately fleeing, it instead retaliates when provoked. Creatures that may belong in the <b>aggressive</b> category but hesitate to a larger or more imposing individual would fall under this classification.
        </li>
        <li>
          <b>Aggressive (<span style="color: #ec5527">A</span>):</b> The creature is very likely to attack a person on site, and is generally not fazed by larger or otherwise more imposing people.
        </li>
      </ul>
      <p>
        This system is mostly used to classify animals, but terminology can also apply to any other life form with the behaviorial qualities needed to meaningfully react to an approaching person.
      </p>
      <p>
        In addition to a Response Index, life forms are also given a "Threat Index," represented by a number between 1-6.
      </p>
        <ul>
          <li>
            <b><span style="color: #b4b4b4">1</span>:</b> Laughably weak. Poses zero offensive threat to most (if not all) people.
          </li>
          <li>
            <b><span style="color: #b4b4b4">2</span>:</b> Can deal considerable damage to someone unfamiliar with the organism.
          </li>
          <li>
            <b><span style="color: #b4b4b4">3</span>:</b> Possesses the strength and intelligence to put up a fight against adventurers.
          </li>
          <li>
            <b><span style="color: #b4b4b4">4</span>:</b> Likely requires a team of experienced fighters to take it down.
          </li>
          <li>
            <b><span style="color: #b4b4b4">5</span>:</b> Intensely powerful. This being has the potential to cause significant damage.
          </li>
          <li>
            <b><span style="color: #b4b4b4">6</span>:</b> Impossible to defeat or control - usually reserved for Beastlords.
          </li>
        </ul>
      <p>
        This additional index was provided due to the early realization that sometimes how aggressive an organism is may not directly correlate with how much damage it can do.
      </p>
      <p>
        An example of a life form with a safer Response ID and a high Threat ID is the paddlet. A large amphibian native to Guashan marshlands, it has been officially classified as <b><span style="color: #21af1d">D</span><span style="color: #b4b4b4">4</span></b>. It's not uncommon for adventurers to be approached by a curious paddlet. Unfortunately, these creatures severely lack depth perception, and often run over their objects of interest by accident <i>(ouch).</i>
      </p>
      <p>
        An example of a life form with a significant Response ID and a lower Threat ID is the acerito, a samara-like creature that can be seen gliding in the forests of Cieliso. It is classified as <b><span style="color: #ec5527">A</span><span style="color: #929292">1</span></b>. Aceritos will immediately launch themselves in the direction of anything that approaches. However, it is very small and paper-like, rendering it unable to cause actual harm to whatever tipped them off.
      </p>
