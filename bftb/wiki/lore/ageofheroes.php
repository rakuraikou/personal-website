        <p>
          The Age of Heroes refers to the era in which the ancient darkness regularly rose to threaten the people of Luxos, only to be halted by a designated hero. Every decade, it would emerge from its den, preparing to consume the light of the world. The people of Luxos knew to send forth their most powerful warrior—someone with immense magical power and unwavering courage—to face it alone. The hero would never return, but the darkness would recede for another decade, leaving the people of Luxos safe from harm.
        </p>
        <p>
          This tradition would continue every decade. A settlement known as "Heroes' Womb" took shape just outside of the ancient darkness' den. There, powerful casters and other prospective heroes lived comfortable lives before embarking on their pilgrimage to the den.
        </p>
        <p>
          It was said that the ancient darkness took the form of an ever-consuming explosion with vaguely dragon-like features. Depictions vary between sources—some depicting a more concrete lifeform with fangs and claws, and others illustrating a jagged black hole. One thing remained consistent, however: whatever the darkness came in contact with would be lost forever.
        </p>
        <p>
          The Age of Heroes ended when the final hero, Loniles de Luna, was selected to face the ancient darkness. With her immense command over magic and the help from allies gained along her journey, Loniles vanquished the ancient darkness using a sword of pure light. When the darkness' form went motionless, a distant bell was rung in victory. With the accursed beast slain, the people of Luxos celebrated, and Loniles went on to form the Kingdom of Cieliso.
        </p>
