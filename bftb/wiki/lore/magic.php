        <h1>
          Magic
        </h1>
        <hr>
        <h2>
          Aspects
        </h2>
        <hr>
        <p>
          An aspect is defined as a specific type of <a href="light">light</a>, manifesting as a single (magical) element. Each <a href="people">person</a> is born with the potential to conjure or manipulate a single aspect, and it cannot be changed. For example, someone with a water aspect is able to transmute pure light into water and control it at will. This person could also control water that is already present, such as from nearby rivers and oceans. 
        </p>
        <p>
          There are nine aspects of light present in Luxos—earth, fire, water, wind, thunder, ice, bloom, radiance, and umbra. What aspect a person has can only be identified when it is casted, and generally cannot be "read" through other means. When identifying individuals or groups who manipulate a specific aspect, the term "wielder" is generally used.
        </p>
        <p class="notebox">
          The ability to use magic isn't just limited to people. All creatures and even some plants can cast as well!
        </p>
        <p>
          Aspects are powerful and versatile, and society has explored many applications and techniques over time. However, despite its great potential, there is a limit to one's ability to use an aspect: as one uses it, a dangerous waste byproduct called "cael" is collected in the bloodstream where light once flowed.
        </p>
        <h2>
          Casting
        </h2>
        <hr>
        <p>
          For most use cases, using magic is about as mundane and painless as any other chore. A fire aspect wielder may stoke a glowing ember to fill their fireplace, or a bloom aspect wielder may twist the youngest branches of their bonsai to perfection. In these use cases, the wielder is manipulating something that is already present, and thus does not need to exert much energy in doing so.
        </p>
        <p>
          When such resources aren't readily available, such as in combat, a different and far more physically-exerting technique is applied. Luxos' atmosphere is composed of a pure light containing every aspect, so wielders are actually able to "extract" their aspect from that very compound and use it however they wish. Though convenient, doing this requires the wielder to expend a small amount of their own light in order to resonate with that in the atmosphere. The absence of this light within the body results in the accumulation of "cael."
        </p>
        <p class="notebox">
          Not all casting is made equal. Those with radiance aspects tend to be unable to cast in the former method due to "radiant entities" not necessarily being a thing (think: stones for earth aspect wielders, stormclouds for thunder wielders, etc.). In turn, radiance wielders have a significantly easier time extracting their aspect from pure light, allowing them to cast spells with far more efficiency. Many radiance aspect wielders go on to become powerful casters, perhaps pursuing magical academia or becoming formidable adversaries on the battlefield.
        </p>
        <h2>
          Cael
        </h2>
        <hr>
        <p>
          Cael is a substance that manifests in the bloodstream in the place of light when magic is used. Like light, the composition of cael is not entirely understood. Low cael buildup manifests as exhaustion, fatigue and muscle aches. Though cael within the body will dispel on its own eventually, rest and hydration will quicken the recovery process. The rate at which people dispel cael and their casting limit before exhibiting any symptoms varies between individuals. Even siblings may find that their cael limits are dramatically different.
        </p>
        <p>
          In higher accumulations, cael may cause an individual suffer a condition known as caelism. Common tells for caelism include severely weakened or impaired bodily functions (such as difficulty breathing or ceased injury recovery) and a telltale "bruising" or "blighting" across various parts of the body. Caelism takes a significant amount of time to run its complete course before it is dispelled, and scarring from the blight may remain even after a full recovery. Medicine exists to increase the rate at which cael is dispelled, but it's better to just never let things get to this point in the first place.
        </p>
        <p>
          Though caelism is very dangerous, prognosis is rarely ever certain death. Caelism initially presents itself in a localized area—if aggravated, it will travel to the heart, where it will likely evolve into a curse or become fatal. Once symptoms are recognized, it is recommended that anyone with caelism find immediate medical care and rest accordingly. Of course, it's also considered common sense to avoid using more magic at all costs once caelism manifests.
        </p>
        <p>
          It's a common belief that spending time around other people, particularly loved ones, increases the rate at which someone recovers. Though concrete explanations are yet to be found, some speculate that this is due to a community survival instinct present in which people subconsciously "share" their light with each other.
        </p>
        <p>
          Another assumption is that the accumulation of cael can be mitigated if there are others there to support, because having multiple wielders actively operating on atmospheric light would reduce the overall effort needed to cast—thus reducing the amount of cael accumulated for everyone. As a result, adventurers and warriors prefer fighting in close proximity of each other (as a "party"). 
        </p>
        <p>
          Both ideas are nice sentiments, regardless of if they are true or not.
        </p>
