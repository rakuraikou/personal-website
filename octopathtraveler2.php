        Welcome to the Octopath Traveler II narrative flowchart! This was an exercise I did in my free time to study video game narrativeâ€”more specifically, how a video game's narrative intersects with its gameplay. It took, literally, hundreds of hours.
      </p>
      <p>
        The intent of this flowchart is to illustrate how player input pushes the story from one plot point to the next. When I first made this chart, I did not put any narrative information that wasn't directly attached to player input (i.e. a flashback cutscene revealing a plot twist). I've decided to retroactively add this information in just for the sake of providing a comprehensive documentation of Octopath Traveler 2's story.
      </p>
      <h3>
        Understanding the format
      </h3>
      <p>
        I've taken a lot of inspiration from <a href="https://cocoatoucher.github.io/earthbound-narrative/">this Earthbound narrative flowchart</a>. Each cell is split into three parts: situation, solution, and conclusion. The situation is the problem presented by the game's story, the solution is the player action required to resolve the problem, and the conclusion is the result of the player action as it impacts the story.
      </p>
      <p>
        <span style="color: #B09500">Yellow</span> text indicates a path action, talent, etc., and <span style="color: #B20000">red</span> text indicates an area with enemy encounters
      </p>
