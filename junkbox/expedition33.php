      <h2>Clair Obscur: Expedition 33 Narrative Flowchart</h2>
      <p>
        Welcome to the Clair Obscur: Expedition 33 narrative flowchart! This was an exercise I did in my free time to study video game narrative—more specifically, how a video game's narrative intersects with its gameplay. Unlike <a href="">the one I made for Octopath Traveler II</a>, this didn't take me hundreds of hours—mostly because the game is much shorter, but also because I was more efficient in how I made this. It's not my first rodeo. As usual, the intent of this flowchart is to illustrate how player input pushes the story from one plot point to the next.
      </p>
      <h3>
        Understanding the format
      </h3>
      <p>
        I've taken a lot of inspiration from <a href="https://cocoatoucher.github.io/earthbound-narrative/" target="_blank">this Earthbound narrative flowchart</a>. Each cell is split into three parts: situation, solution, and conclusion. The situation is the problem presented by the game's story, the solution is the player action required to resolve the problem, and the conclusion is the result of the player action as it impacts the story.
      </p>
      <h3>
        Why did I do this?
      </h3>
      <p>
        Because it was fun.
        <br>
        No, really. I love video games, and I love thinking about them as a storytelling device and art form. This is fun for me.
      </p>
      <p>
        It also helped reshape the way I thought about video game stories, since I was really struggling with writing my own (yeah, I'm working on a game lol...). I highly recommend doing a study like this if you're working on your own game, or just want to gain a better understanding of game narrative and game design!
      </p>
      <p>
        I actually have some rather complicated feelings about Expedition 33's story. You can read all about it in <a href="https://backloggd.com/u/LEN/review/3703862/">my Backloggd review</a> (warning for spoilers). Vague TL;DR - it had some very glaring issues that I couldn't forgive, even if I enjoyed a lot of it.
      </p>
      <h3>
        Other Notes
      </h3>
      <ul>
        <li>
